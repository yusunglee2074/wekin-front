  (4) 서비스를 이용할 권리를 양도하여 영리를 추구하는 행위
  (5) 정보통신망법 등 관련 법령에 의하여 그 전송 또는 게시가 금지되는 정보(컴퓨터 프로그램 등)을 전송하거나 게시 및 관련 사이트를 링크하는 행위
  (6) 회사 및 기타 제3자의 저작권 등 지적 재산권에 대한 침해
  (7) 회사 및 기타 제3자의 명예를 손상시키거나 업무를 방해하는 행위(예-회사의 직원이나 서비스의 관리자로 가장, 사칭하거나 타인의 명의를 도용하여 글을 게시하거나 메일을 발송하는 행위)
  (8) 다른 회원의 아이디 및 비밀번호를 도용하여 서비스를 이용하는 행위
  (9) 회사에서 물품이나 서비스를 이용하는 대가로 타인의 계좌번호, 신용카드 등의 결제정보를 허락 없이 이용하는 행위
