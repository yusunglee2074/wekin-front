제16조(포인트 등)
 ① 포인트
  가. 포인트(명칭 여하 불문 포인트로 인정될 수 있는 것 모두 포함)는 회사가 운영하는 사이버 화폐로서 회사가 운영하는 서비스 등을 구매할 때 결제 수단으로 사용할 수 있다.
  나. 적립금의 유효기간은 적립전환 후 12개월이며, 유효기간은 월별로 관리되고 지급된 월로부터 12개월이 지난 후 해당 월의 말일에 자동적으로 소멸된다. 단, 구매취소로 인하여 재지급되는 적립금의 유효기간은 재지급시점을 기준으로 12개월이고, 기타 마케팅 및 프로모션을 통하여 지급되는 포인트의 유효기간은 각각 별도로 설정될 수 있다.
  다. 포인트를 부여할 때 사용기간에 대한 별도의 사전고지 또는 특약이 있는 경우에는 그 사용기간 이내에 사용하여야 하고, 유효기간 이내에 사용하지 않을 경우 순차적으로 소멸된다.
  라. 포인트의 사용은 만료기간이 빠른 순서로 사용되며, 유효기간이 만료되거나 이용계약이 종료되면 적립 전환된 포인트 중 미사용한 적립금은 소멸한다.

 ② 쿠폰
  가. 쿠폰은 발행대상, 발행경로, 사용대상 등에 따라 구분될 수 있으며, 쿠폰의 세부구분, 할인금액(할인율), 사용방법, 사용기한 및 제한에 대한 사항은 할인쿠폰 또는 서비스화면에 표시된다. 쿠폰의 종류 및 내용과 발급여부에 관하여는 회사의 각 사이트에 대한 정책에 따라 달라질 수 있다.
  나. 쿠폰은 현금으로 출금될 수 없으며, 쿠폰에 표시된 유효기간이 만료되거나 이용계약이 종료되면 소멸한다.
  다. 쿠폰은 회사에서 별도로 명시한 경우를 제외하고는 타인에게 양도할 수 없으며, 부정한 목적이나 용도로 사용할 수 없다. 이를 위반한 경우 회사는 쿠폰을 회수하거나 회원자격을 정지할 수 있다.

제17조(서비스의 이용)
 ① 회원은 개별조건상에 명시된 유효 기간 내에 한해서만 업체의 서비스를 제공받을 수 있다. 회원이 유효기간 이내에 서비스를 이용하지 못한 경우 회사의 환불정책에 따라 일정 비율의 금액만 환불받을 수 있다.
 ② 회원은 회사 홈페이지에 명시된 내용 및 개별 주의사항(이용조건)을 확인하여야 할 책임이 있으며, 명시된 이용조건에 따라서만 구매한 상품을 이용할 수 있다

