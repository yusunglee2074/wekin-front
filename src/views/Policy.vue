  (11) 다른 회원의 개인정보를 그 동의 없이 수집, 저장, 공개, 유포하는 행위
  (12) 기타 현행 법령, 회사가 제공하는 서비스에 대한 약관 기타 서비스 이용에 관한 규정 및 회사 방침(사이트 공지사항 포함)에 위반하는 행위
제11조(회사의 의무)

