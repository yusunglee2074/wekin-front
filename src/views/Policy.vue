 ① 서비스의 이용은 원칙적으로 1일 24시간을 원칙으로 합니다(연중무휴). 다만 정기점검 등의 사유로 회사가 정한 시간은 서비스 이용이 중단될 수 있습니다.
 ② 1항에서 정한 서비스 제공 중단 사유는 아래와 같습니다.
