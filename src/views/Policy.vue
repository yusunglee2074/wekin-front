 ② 회사는 회원이 구매한 서비스 등에 대해 이용방법, 이용기간 등을 명시한다.
 ③ 회사와 고객 간에 서비스의 인도시기 및 쿠폰의 제공시기에 관하여 별도의 약정이 있는 경우에는 이 약관에 우선한다.

